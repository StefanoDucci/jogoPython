def criar_bolsa(bolsa):
    bolsa.append(1) #0  Level poção de vida
    bolsa.append(30)#1  Valor recupera de vida
    bolsa.append(5) #2  Numero maximo de poção de vida
    bolsa.append(0) #3  Numero atual de poção de vida
    
    bolsa.append(1) #4  Level poção de mana
    bolsa.append(20)#5  Valor recupera de mana
    bolsa.append(5) #6  Numero maximo de poção de mana
    bolsa.append(0) #7  Numero atual de poção de mana

    bolsa.append(1) #8  Level de Critico
    bolsa.append(10)#9  Valor de Critico
    bolsa.append(3) #10 Numero maximo de Critico
    bolsa.append(0) #11 Numero atual de Critico

    bolsa.append(1) #12 Indice de Antidoto
    bolsa.append(5) #13 Numero maximo de Antidoto
    bolsa.append(5) #14 Numero atual de Antidoto
    
    bolsa.append(1) #15  Level poção de Hibrida
    bolsa.append(15)#16  Valor recupera de vida
    bolsa.append(15)#17  Valor recupera de mana
    bolsa.append(3) #18  Numero maximo de poção Hibrida
    bolsa.append(3) #19  Numero atual de poção Hibrida
